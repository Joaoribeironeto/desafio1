
float a = 3.8f;

float b = 8.2f;
void OperacoesBasicas(){

    float adicao = a + b;
    Console.WriteLine($"\n Adição = {adicao} ");
    
    float subtracao = a - b;
    Console.WriteLine($"\n Subtração = {subtracao} ");

    float multiplicacao = a * b;
    Console.WriteLine($"\n Multiplicação = {multiplicacao} ");

    float divisao = a / b;
    Console.WriteLine($"\n Divisão = {divisao} ");
}

OperacoesBasicas();